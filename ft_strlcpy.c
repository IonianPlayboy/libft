#include "libft.h"

size_t	ft_strlcpy(char *dst, char const *src, size_t dstsize)
{
	unsigned int	src_size;
	unsigned int	i;

	src_size = 0;
	while (src[src_size])
		src_size++;
	if (dstsize == 0)
		return (src_size);
	i = 0;
	while (i < dstsize - 1 && src[i])
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (src_size);
}
