#include "libft.h"

char	*ft_strdup(char const *src)
{
	int		i;
	int		length;
	char	*result;

	length = 0;
	while (src[length])
		length++;
	if (!(result = (char *)malloc(sizeof(char) * (length + 1))))
		return (NULL);
	i = 0;
	while (src[i])
	{
		result[i] = src[i];
		i++;
	}
	result[i] = '\0';
	return (result);
}
